"""Track A Phase 2 — Materials Project から PBE 文献値を一括取得.

各材料の reference データを ``materials/references/{formula}.toml`` に
書く。Tier C (ドープ Si) は MP に存在しないので自動スキップ。

書き込み内容:
    formula = "Si"
    n_atoms = 2
    is_insulator = true / false
    band_gap_PBE_eV = 0.611                     # MP の PBE-GGA gap
    band_gap_source = "Materials Project mp-149"
    # e_total_converged_Ry_per_atom はここでは書かない (自前 sweep で別途登録)

実行方法:
    export MP_API_KEY=...   # https://next-gen.materialsproject.org/api
    python3 scripts/fetch_mp_references.py                    # 全 tier_a/b 取得
    python3 scripts/fetch_mp_references.py --formulas Si,Ge   # 特定材料のみ
    python3 scripts/fetch_mp_references.py --tier A           # Tier A のみ
    python3 scripts/fetch_mp_references.py --dry-run          # API 叩かず計画だけ表示
"""

from __future__ import annotations

import argparse
import sys
import time
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
sys.path.insert(0, str(REPO_ROOT))

from orchestrator.materials import load_tier, Material
from orchestrator.mp_client import MPClient, MPSummary

REF_DIR = REPO_ROOT / "materials" / "references"


def is_insulator_from_mp(s: MPSummary) -> bool:
    """MP の is_metal フラグと band_gap から絶縁体判定.

    MP は is_metal を bool で返す (None もあり得る)。
    is_metal=True なら金属、False かつ gap > 0 なら絶縁体/半導体。
    """
    if s.is_metal is True:
        return False
    if s.band_gap is not None and s.band_gap > 0.0:
        return True
    return False


def render_toml(formula: str, mp_id: str, summary: MPSummary, n_atoms: int) -> str:
    insulator = is_insulator_from_mp(summary)
    gap = summary.band_gap if summary.band_gap is not None else 0.0
    gap_repr = f"{gap:.4f}" if summary.band_gap is not None else "0.0  # 金属"
    direct = ""
    if summary.is_gap_direct is not None:
        direct = f"# direct gap: {summary.is_gap_direct}\n"
    src = f"Materials Project {mp_id} (PBE-GGA)"

    return (
        f"# Auto-generated by scripts/fetch_mp_references.py\n"
        f"# 取得時刻: {time.strftime('%Y-%m-%dT%H:%M:%SZ', time.gmtime())}\n"
        f"# {direct}"
        f"formula = \"{formula}\"\n"
        f"n_atoms = {n_atoms}\n"
        f"is_insulator = {'true' if insulator else 'false'}\n"
        f"band_gap_PBE_eV = {gap_repr}\n"
        f"band_gap_source = \"{src}\"\n"
        f"# e_total_converged_Ry_per_atom = ...    # 自前 ecut sweep で後から追記\n"
    )


def safe_filename(formula: str, structure: str) -> str:
    """同じ formula で polymorph 違い (TiO2 anatase vs rutile) を区別.

    formula にスラッシュ系記号が無いことだけ確認 (Si63P1 等は OK)。
    polymorph キーワードがあれば suffix する。
    """
    if "/" in formula or " " in formula:
        formula = formula.replace("/", "_").replace(" ", "_")
    keys = ("anatase", "rutile", "wurtzite", "zincblende", "hexagonal",
            "diamond", "perovskite", "monolayer")
    for k in keys:
        if k in structure.lower() and k not in formula.lower():
            return f"{formula}_{k}"
    return formula


def materials_to_fetch(
    tier: str | None,
    formulas: list[str] | None,
) -> list[Material]:
    out: list[Material] = []
    tiers = [tier] if tier else ["A", "B"]   # Tier C はスキップ (MP なし)
    for t in tiers:
        for m in load_tier(t):
            if formulas and m.formula not in formulas:
                continue
            if not m.mp_id:
                continue
            out.append(m)
    return out


