def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--tier", choices=["A", "B"])
    ap.add_argument("--formulas", help="comma-separated formula list filter")
    ap.add_argument("--dry-run", action="store_true")
    ap.add_argument("--rate-sleep", type=float, default=0.5,
                    help="seconds between MP API calls (be kind)")
    args = ap.parse_args()

    formulas = [f.strip() for f in args.formulas.split(",")] if args.formulas else None
    targets = materials_to_fetch(args.tier, formulas)

    print(f"== fetch_mp_references == ({len(targets)} materials)")
    for m in targets:
        print(f"  {m.tier}  {m.formula:20}  {m.mp_id:15}  {m.structure}")

    if args.dry_run:
        print("\n(dry-run, no API calls)")
        return 0

    REF_DIR.mkdir(parents=True, exist_ok=True)

    try:
        client = MPClient()
    except RuntimeError as e:
        print(f"ERROR: {e}", file=sys.stderr)
        return 2

    ok = 0
    failed: list[str] = []
    for m in targets:
        out_name = safe_filename(m.formula, m.structure) + ".toml"
        out_path = REF_DIR / out_name
        try:
            s = client.summary(m.mp_id)
            n_sites = s.nsites
            if n_sites is None:
                # 古い MP API client が _fields に nsites を含めていない場合の fallback
                n_sites = s.raw.get("nsites")
            if n_sites is None:
                print(f"  WARN: {m.mp_id}: nsites not in summary, leaving 0")
            content = render_toml(
                formula=m.formula,
                mp_id=m.mp_id,
                summary=s,
                n_atoms=int(n_sites) if n_sites else 0,
            )
            out_path.write_text(content)
            label = "insulator" if is_insulator_from_mp(s) else "metal"
            gap_str = f"{s.band_gap:.3f}" if s.band_gap is not None else "n/a"
            print(f"  OK  {m.formula:20}  gap={gap_str} eV  {label}  → {out_name}")
            ok += 1
        except Exception as e:
            print(f"  FAIL {m.formula:20}  {m.mp_id}  {e}")
            failed.append(m.formula)
        time.sleep(args.rate_sleep)

    print(f"\nDone: ok={ok} failed={len(failed)}")
    if failed:
        print("Failed:", ", ".join(failed))
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
